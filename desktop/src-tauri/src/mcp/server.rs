// ─── MCP HTTP server (axum) ───────────────────────────────────────────────────
//
// Implements the Model Context Protocol JSON-RPC 2.0 server.
// Protocol version: 2024-11-05
//
// Endpoints:
//   POST /     → JSON-RPC 2.0 dispatch (all MCP methods)
//   GET  /health → {"status": "ok", "server": "loci-mcp", "version": "0.1.0"}
//
// Supported MCP methods:
//   initialize            → handshake, returns capabilities
//   resources/list        → lists all loci:// resources
//   resources/read        → reads a single resource by URI
//   resources/templates/list → lists URI templates
//   tools/list            → lists available tools
//   tools/call            → dispatches create_locus or tag_locus
//
// Cipher gate:
//   - Binds to 127.0.0.1:{port} only. Never 0.0.0.0.
//   - All responses include `X-Loci-Content-Trust: user-authored`.
//   - THREAT-01: no Conversation data in any response path.
//   - Port range enforced before bind: 1024–65535.
//   - Graceful shutdown via tokio oneshot channel.

use axum::{
    extract::State,
    http::{HeaderMap, HeaderValue, StatusCode},
    response::IntoResponse,
    routing::{get, post},
    Json, Router,
};
use serde::{Deserialize, Serialize};
use serde_json::{json, Value};
use std::net::SocketAddr;
use std::path::PathBuf;
use std::sync::Arc;
use tokio::net::TcpListener;
use tokio::sync::oneshot;

use super::resources;
use super::tools;

// ─── Server state ────────────────────────────────────────────────────────────

/// Shared state injected into every axum handler.
#[derive(Clone)]
pub struct McpServerState {
    /// Base path for the loci store (e.g. `~/.loci/`).
    pub loci_base: PathBuf,
    /// Room IDs exposed via MCP. Empty = all rooms exposed. Non-empty = allowlist.
    /// Enforced by resource handlers — agents can only read loci from listed rooms.
    /// Cipher gate: this value is set at server start and immutable for the server lifetime.
    pub expose_rooms: Vec<String>,
}

// ─── JSON-RPC types ───────────────────────────────────────────────────────────

#[derive(Debug, Deserialize)]
struct JsonRpcRequest {
    #[allow(dead_code)]
    jsonrpc: String,
    id: Option<Value>,
    method: String,
    #[serde(default)]
    params: Value,
}

#[derive(Debug, Serialize)]
struct JsonRpcResponse {
    jsonrpc: String,
    id: Option<Value>,
    #[serde(skip_serializing_if = "Option::is_none")]
    result: Option<Value>,
    #[serde(skip_serializing_if = "Option::is_none")]
    error: Option<JsonRpcError>,
}

#[derive(Debug, Serialize)]
struct JsonRpcError {
    code: i32,
    message: String,
}

impl JsonRpcResponse {
    fn ok(id: Option<Value>, result: Value) -> Self {
        Self { jsonrpc: "2.0".to_string(), id, result: Some(result), error: None }
    }

    fn err(id: Option<Value>, code: i32, message: impl Into<String>) -> Self {
        Self {
            jsonrpc: "2.0".to_string(),
            id,
            result: None,
            error: Some(JsonRpcError { code, message: message.into() }),
        }
    }
}

// ─── Cipher gate: security headers ───────────────────────────────────────────

/// Returns the security headers applied to every MCP response.
/// `X-Loci-Content-Trust: user-authored` signals to consuming agents that all
/// content in this response was authored by the user, not generated by an LLM.
/// This is the THREAT-01 attestation header.
fn loci_security_headers() -> HeaderMap {
    let mut headers = HeaderMap::new();
    headers.insert(
        "X-Loci-Content-Trust",
        HeaderValue::from_static("user-authored"),
    );
    headers.insert(
        "X-Loci-Threat-Gate",
        HeaderValue::from_static("THREAT-01:enforced"),
    );
    headers
}

// ─── MCP method handlers ──────────────────────────────────────────────────────

fn handle_initialize(id: Option<Value>) -> JsonRpcResponse {
    JsonRpcResponse::ok(
        id,
        json!({
            "protocolVersion": "2024-11-05",
            "capabilities": {
                "resources": {
                    "subscribe": false,
                    "listChanged": false
                },
                "tools": {}
            },
            "serverInfo": {
                "name": "loci-mcp",
                "version": "0.1.0",
                "description": "Loci knowledge garden — MCP server. Exposes user-authored Locus nodes for AI agent consumption."
            }
        }),
    )
}

fn handle_resources_list(id: Option<Value>, state: &McpServerState) -> JsonRpcResponse {
    let resources = resources::list_loci(&state.loci_base, &state.expose_rooms);
    JsonRpcResponse::ok(id, json!({ "resources": resources }))
}

fn handle_resources_templates_list(id: Option<Value>) -> JsonRpcResponse {
    JsonRpcResponse::ok(id, json!({
        "resourceTemplates": [
            {
                "uriTemplate": "loci://locus/{id}",
                "name": "Locus node by ID",
                "mimeType": "text/markdown",
                "description": "A single user-authored Locus node. Substitute {id} with the locus ID (e.g. locus-2026-05-09-sovereignty)."
            },
            {
                "uriTemplate": "loci://room/{roomId}/loci",
                "name": "All Loci in a Room",
                "mimeType": "application/json",
                "description": "Returns all Locus nodes belonging to the given Room. Substitute {roomId} with the room identifier."
            },
            {
                "uriTemplate": "loci://search?q={query}",
                "name": "Search Loci",
                "mimeType": "application/json",
                "description": "Keyword search over Locus titles and content. Returns up to 20 ranked results."
            }
        ]
    }))
}

fn handle_resources_read(id: Option<Value>, params: &Value, state: &McpServerState) -> JsonRpcResponse {
    let uri = match params.get("uri").and_then(|v| v.as_str()) {
        Some(u) => u,
        None => return JsonRpcResponse::err(id, -32602, "missing required param: uri"),
    };

    // Route by URI scheme
    if let Some(rest) = uri.strip_prefix("loci://locus/") {
        match resources::read_locus(rest, &state.loci_base, &state.expose_rooms) {
            Some(r) => JsonRpcResponse::ok(id, json!({
                "contents": [{
                    "uri": r.uri,
                    "mimeType": r.mime_type,
                    "text": r.text,
                    "metadata": r.metadata,
                }]
            })),
            None => JsonRpcResponse::err(id, -32002, format!("locus not found: {}", rest)),
        }
    } else if let Some(rest) = uri.strip_prefix("loci://room/") {
        // loci://room/{roomId}/loci
        let room_id = rest.trim_end_matches("/loci");
        // Cipher gate: check room is exposed before listing its loci
        if !state.expose_rooms.is_empty() && !state.expose_rooms.iter().any(|r| r == room_id) {
            return JsonRpcResponse::err(id, -32002, format!("room '{}' is not exposed via MCP", room_id));
        }
        let loci = resources::list_room_loci(room_id, &state.loci_base, &state.expose_rooms);
        let contents: Vec<Value> = loci
            .into_iter()
            .map(|r| json!({
                "uri": r.uri,
                "mimeType": r.mime_type,
                "text": r.text,
                "metadata": r.metadata,
            }))
            .collect();
        JsonRpcResponse::ok(id, json!({ "contents": contents }))
    } else if let Some(rest) = uri.strip_prefix("loci://search?q=") {
        let results = resources::search_loci(rest, &state.loci_base, &state.expose_rooms);
        let contents: Vec<Value> = results
            .into_iter()
            .map(|r| json!({
                "uri": r.uri,
                "mimeType": r.mime_type,
                "text": r.text,
                "metadata": r.metadata,
            }))
            .collect();
        JsonRpcResponse::ok(id, json!({ "contents": contents }))
    } else {
        JsonRpcResponse::err(id, -32002, format!("unknown loci URI scheme: {}", uri))
    }
}

