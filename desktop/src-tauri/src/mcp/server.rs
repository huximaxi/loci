fn handle_tools_call(id: Option<Value>, params: &Value, state: &McpServerState) -> JsonRpcResponse {
    let name = match params.get("name").and_then(|v| v.as_str()) {
        Some(n) => n,
        None => return JsonRpcResponse::err(id, -32602, "missing required param: name"),
    };
    let args = params.get("arguments").unwrap_or(&Value::Null);

    match name {
        "create_locus" => {
            let title = match args.get("title").and_then(|v| v.as_str()) {
                Some(t) => t,
                None => return JsonRpcResponse::err(id, -32602, "create_locus: missing required argument: title"),
            };
            let content = match args.get("content").and_then(|v| v.as_str()) {
                Some(c) => c,
                None => return JsonRpcResponse::err(id, -32602, "create_locus: missing required argument: content"),
            };
            let tags: Vec<String> = args
                .get("tags")
                .and_then(|v| v.as_array())
                .map(|a| a.iter().filter_map(|t| t.as_str().map(|s| s.to_string())).collect())
                .unwrap_or_default();
            let room_id = args.get("room_id").and_then(|v| v.as_str());

            match tools::create_locus(title, content, &tags, room_id, &state.loci_base) {
                Ok(uri) => JsonRpcResponse::ok(id, json!({
                    "content": [{
                        "type": "text",
                        "text": format!("Created: {}", uri)
                    }]
                })),
                Err(e) => JsonRpcResponse::err(id, -32000, format!("create_locus failed: {}", e)),
            }
        }

        "tag_locus" => {
            let locus_id = match args.get("id").and_then(|v| v.as_str()) {
                Some(i) => i,
                None => return JsonRpcResponse::err(id, -32602, "tag_locus: missing required argument: id"),
            };
            let tags: Vec<String> = match args.get("tags").and_then(|v| v.as_array()) {
                Some(a) => a.iter().filter_map(|t| t.as_str().map(|s| s.to_string())).collect(),
                None => return JsonRpcResponse::err(id, -32602, "tag_locus: missing required argument: tags"),
            };

            match tools::tag_locus(locus_id, &tags, &state.loci_base) {
                Ok(()) => JsonRpcResponse::ok(id, json!({
                    "content": [{
                        "type": "text",
                        "text": format!("Tags updated on locus '{}'", locus_id)
                    }]
                })),
                Err(e) => JsonRpcResponse::err(id, -32000, format!("tag_locus failed: {}", e)),
            }
        }

        other => JsonRpcResponse::err(id, -32601, format!("unknown tool: {}", other)),
    }
}

// ─── axum handler ────────────────────────────────────────────────────────────

async fn mcp_rpc(
    State(state): State<Arc<McpServerState>>,
    Json(request): Json<JsonRpcRequest>,
) -> impl IntoResponse {
    let id = request.id.clone();

    let response = match request.method.as_str() {
        "initialize" => handle_initialize(id),
        "resources/list" => handle_resources_list(id, &state),
        "resources/read" => handle_resources_read(id, &request.params, &state),
        "resources/templates/list" => handle_resources_templates_list(id),
        "tools/list" => handle_tools_list(id),
        "tools/call" => handle_tools_call(id, &request.params, &state),
        // Notifications (no response required by MCP spec, but we ack)
        "notifications/initialized" => return (loci_security_headers(), StatusCode::NO_CONTENT).into_response(),
        other => JsonRpcResponse::err(id, -32601, format!("method not found: {}", other)),
    };

    (loci_security_headers(), Json(response)).into_response()
}

async fn health_check() -> impl IntoResponse {
    (
        loci_security_headers(),
        Json(json!({
            "status": "ok",
            "server": "loci-mcp",
            "version": "0.1.0",
            "protocol": "2024-11-05",
            "threat_gate": "THREAT-01:enforced"
        })),
    )
}

// ─── Public: server lifecycle ─────────────────────────────────────────────────

/// Start the MCP server on `localhost:{port}`.
/// Returns the actual bound port (useful when port=0 for OS assignment).
/// The server runs until `shutdown_rx` fires.
pub async fn start_server(
    port: u16,
    loci_base: PathBuf,
    expose_rooms: Vec<String>,
    shutdown_rx: oneshot::Receiver<()>,
) -> Result<u16, String> {
    let state = Arc::new(McpServerState { loci_base, expose_rooms });

    let app = Router::new()
        .route("/", post(mcp_rpc))
        .route("/health", get(health_check))
        .with_state(state);

    // Bind to 127.0.0.1 only — Cipher gate, non-negotiable.
    let addr = SocketAddr::from(([127, 0, 0, 1], port));
    let listener = TcpListener::bind(addr)
        .await
        .map_err(|e| format!("failed to bind MCP server to {}: {}", addr, e))?;

    let actual_port = listener
        .local_addr()
        .map(|a| a.port())
        .map_err(|e| e.to_string())?;

    tokio::spawn(async move {
        axum::serve(listener, app)
            .with_graceful_shutdown(async { let _ = shutdown_rx.await; })
            .await
            .ok();
    });

    Ok(actual_port)
}
